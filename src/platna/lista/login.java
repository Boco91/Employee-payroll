    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String sql ="select korisnik, sifra, pozicija from Korisnici where(korisnik = ? and sifra = ? and pozicija=?)";

        try{
            int count =0;
            pst = conn.prepareStatement(sql);

            pst.setString(1, txt_korisnik.getText());
            pst.setString(2, txt_sifra.getText());
            pst.setString(3, txt_combo.getSelectedItem().toString());

            rs=pst.executeQuery();
            while(rs.next()){

                int id =rs.getInt(1);
                Rdk.rdkId = id;
                count = count+1;
            }

            String ulaz =(txt_combo.getSelectedItem().toString());

            if (ulaz=="Admin"){
                if (count==1){
                    JOptionPane.showMessageDialog(null, "Uspjesno ste se prijavili.");
                    GlavniMeni m =new GlavniMeni();
                    m.setVisible(true);
                    this.dispose();

                }else{
                    JOptionPane.showMessageDialog(null, "Korisnicko ime ili sifra su pogresni");
                }
            }

        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        finally{

            try{
                rs.close();
                pst.close();

            }catch (Exception e){

            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed

